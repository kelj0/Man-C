#include <stdio.h>

int main(){
    printf("1+1 = %d\n",1+1);
    printf("1-1 = %d\n",1-1);
    printf("2*3 = %d\n",2*3);
    printf("2.0*3 = %d\n",2.0*3);
    printf("3/2 = %d\n", 3/2);
    printf("3.0/2 = %d\n", 3.0/2);
    printf("4/2 = %d\n",4/2);
    printf("4.0/2 = %d\n",4.0/2);
    printf("5%3 = %d\n",5%3);
    printf("5.0%3 = %d\n",5%3);
    printf("2^3 = %d\n",2^3);
    printf("5^1 = %d\n",5^1);
    printf("1|2 = %d\n",2|1);
    printf("1&2 = %d\n",2&1);
    printf("2<<1 = %d\n",2<<1);
    printf("2>>1 = %d\n",2>>1);
    return 0;
}
